"""
# ╔════════════════════════════════════════════════
# ║  CELL 6 — Advanced Feature Extraction                               
# ║                                                                     
# ║  Instead of extracting a flat feature vector, I'm building a       
# ║  3-channel "spectrogram image" per audio file:                      
# ║                                                                     
# ║   Channel 0 → MFCC          (static timbre snapshot)               
# ║   Channel 1 → Δ  MFCC       (how timbre is changing)               
# ║   Channel 2 → ΔΔ MFCC       (rate of change of change)             
# ║                                                                     
# ║  This is exactly how state-of-the-art speech models encode audio.  
# ║  Shape per sample: (N_MFCC=40, TIME_STEPS=130, channels=3)         
# ╚══════════════════════════════════════════════════"""

def normalize_feature(x):
    """Zero-mean, unit-variance normalization per feature map."""
    return (x - x.mean()) / (x.std() + 1e-8)


def fix_time_dim(x, length=TIME_STEPS):
    """Pad with zeros or truncate to a fixed number of time frames."""
    if x.shape[1] < length:
        pad_width = length - x.shape[1]
        x = np.pad(x, ((0,0),(0,pad_width)), mode='constant', constant_values=0)
    return x[:, :length]


def spec_augment(feature_3ch):
    """
    SpecAugment (Park et al., 2019) — randomly masks time frames
    and frequency bands to force the model to be more robust.
    This is applied ONLY during training, not at inference.
    """
    out = feature_3ch.copy()
    F, T, C = out.shape

    # Time masking: zero out random contiguous time frames
    for _ in range(2):
        t  = np.random.randint(0, max(1, SPEC_TIME_MASK))
        t0 = np.random.randint(0, max(1, T - t))
        out[:, t0:t0+t, :] = 0.0

    # Frequency masking: zero out random contiguous frequency bands
    for _ in range(2):
        f  = np.random.randint(0, max(1, SPEC_FREQ_MASK))
        f0 = np.random.randint(0, max(1, F - f))
        out[f0:f0+f, :, :] = 0.0

    return out


def audio_augment(y):
    """
    Waveform-level augmentation — randomly applies one of four methods.
    This doubles the variety in our training set without extra recordings.
    """
    choice = np.random.choice(['none','stretch','pitch','noise'])
    if choice == 'stretch':
        rate = np.random.uniform(*TIME_STRETCH_RANGE)
        y    = librosa.effects.time_stretch(y, rate=rate)
    elif choice == 'pitch':
        steps = np.random.randint(*PITCH_SHIFT_RANGE)
        y     = librosa.effects.pitch_shift(y, sr=SAMPLE_RATE, n_steps=int(steps))
    elif choice == 'noise':
        y = y + NOISE_LEVEL * np.random.randn(len(y))
    return librosa.util.fix_length(y, size=int(SAMPLE_RATE * DURATION))


def extract_3ch_feature(file_path, augment=False):
    """
    Core feature extraction function.
    Loads audio → optionally augments → extracts 3-channel MFCC image.

    Returns:
        np.ndarray of shape (N_MFCC, TIME_STEPS, 3)  or  None on error
    """
    try:
        y, _ = librosa.load(file_path, sr=SAMPLE_RATE, duration=DURATION)
        y    = librosa.util.fix_length(y, size=int(SAMPLE_RATE * DURATION))

        if augment:
            y = audio_augment(y)

        # ── Extract MFCC and its first and second derivatives ──────────
        mfcc    = librosa.feature.mfcc(y=y, sr=SAMPLE_RATE, n_mfcc=N_MFCC,
                                        n_fft=N_FFT, hop_length=HOP_LENGTH)
        delta   = librosa.feature.delta(mfcc)
        delta2  = librosa.feature.delta(mfcc, order=2)

        # ── Normalize each feature map independently ────────────────────
        mfcc   = normalize_feature(mfcc)
        delta  = normalize_feature(delta)
        delta2 = normalize_feature(delta2)

        # ── Pad or truncate to fixed TIME_STEPS ────────────────────────
        mfcc   = fix_time_dim(mfcc)
        delta  = fix_time_dim(delta)
        delta2 = fix_time_dim(delta2)

        # ── Stack as 3-channel image: (40, 130, 3) ─────────────────────
        feature_3ch = np.stack([mfcc, delta, delta2], axis=-1).astype('float32')

        # Apply SpecAugment during training
        if augment:
            feature_3ch = spec_augment(feature_3ch)

        return feature_3ch

    except Exception as exc:
        print(f"⚠️  Skipping {os.path.basename(file_path)}: {exc}")
        return None


