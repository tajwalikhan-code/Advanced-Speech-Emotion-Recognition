"""

# ╔══════════════════════════════════════════════
# ║  CELL 10 — Advanced Model Architecture                              
# ║                                                                     
# ║  SE-CNN  → Multi-Head Self-Attention  → BiLSTM  → Temporal Attn   
# ║                                                                     
# ║  Input (40, 130, 3)                                                 
# ║    ↓ SE-CNN Block 1 (64 filters + channel attention)                
# ║    ↓ SE-CNN Block 2 (128 filters + channel attention)               
# ║    ↓ SE-CNN Block 3 (256 filters + channel attention)               
# ║    ↓ Permute + Pool → (batch, time=32, features=256)               
# ║    ↓ Multi-Head Self-Attention (4 heads, key_dim=64)               
# ║    ↓ Add + LayerNorm (residual connection)                          
# ║    ↓ BiLSTM (128 units, forward + backward)                         
# ║    ↓ BiLSTM (64 units, forward + backward)                          
# ║    ↓ Temporal Attention Pooling (soft-weight over time)             
# ║    ↓ Dense(256) → BN → Dropout(0.4)                                
# ║    ↓ Dense(128) → Dropout(0.3)                                      
# ║    ↓ Dense(8, softmax) → 8 emotion probabilities                   
# ╚═══════════════════════════════════════════════"""

def freq_pooling(t):
    return tf.reduce_mean(t, axis=2)

def temporal_pooling(t):
    return tf.reduce_sum(t, axis=1)

def se_block(x, ratio=16, name=''):
    channels = x.shape[-1]
    r = max(1, channels // ratio)
    se = layers.GlobalAveragePooling2D(name=f'{name}_squeeze')(x)
    se = layers.Dense(r, activation='relu', name=f'{name}_excite_fc1')(se)
    se = layers.Dense(channels, activation='sigmoid', name=f'{name}_excite_fc2')(se)
    se = layers.Reshape((1, 1, channels), name=f'{name}_reshape')(se)
    return layers.Multiply(name=f'{name}_scale')([x, se])

def cnn_block(x, filters, pool_size=(2,2), dropout_rate=0.25, name=''):
    x = layers.Conv2D(filters, (3,3), padding='same', activation='relu', name=f'{name}_conv')(x)
    x = layers.BatchNormalization(name=f'{name}_bn')(x)
    x = se_block(x, ratio=max(1, filters//16), name=f'{name}_se')
    x = layers.MaxPooling2D(pool_size, name=f'{name}_pool')(x)
    x = layers.Dropout(dropout_rate, name=f'{name}_drop')(x)
    return x

def build_advanced_model(input_shape=(N_MFCC, TIME_STEPS, 3), num_classes=8):
    inputs = keras.Input(shape=input_shape, name='mfcc_3ch_input')
    x = cnn_block(inputs, filters=64, pool_size=(2,2), dropout_rate=0.20, name='cnn1')
    x = cnn_block(x, filters=128, pool_size=(2,2), dropout_rate=0.25, name='cnn2')
    x = cnn_block(x, filters=256, pool_size=(2,1), dropout_rate=0.30, name='cnn3')
    x = layers.Permute((2, 1, 3), name='permute_time_first')(x)
    x = layers.Lambda(freq_pooling, output_shape=(32, 256), name='freq_avg_pool')(x)
    attn_out = layers.MultiHeadAttention(num_heads=4, key_dim=64, dropout=0.10, name='mhsa')(query=x, value=x, key=x)
    x = layers.Add(name='attn_residual')([x, attn_out])
    x = layers.LayerNormalization(name='attn_layernorm')(x)
    x = layers.Bidirectional(layers.LSTM(128, return_sequences=True, dropout=0.25), name='bilstm1')(x)
    x = layers.Bidirectional(layers.LSTM(64, return_sequences=True, dropout=0.20), name='bilstm2')(x)
    attn_scores = layers.Dense(1, activation='tanh', name='temporal_scores')(x)
    attn_weights = layers.Softmax(axis=1, name='temporal_weights')(attn_scores)
    x = layers.Multiply(name='weighted_seq')([x, attn_weights])
    x = layers.Lambda(temporal_pooling, output_shape=(128,), name='temporal_pool')(x)
    x = layers.Dense(256, activation='relu', name='embedding')(x)
    x = layers.BatchNormalization(name='head_bn')(x)
    x = layers.Dropout(0.40, name='head_drop1')(x)
    x = layers.Dense(128, activation='relu', name='dense2')(x)
    x = layers.Dropout(0.30, name='head_drop2')(x)
    out = layers.Dense(num_classes, activation='softmax', dtype='float32', name='predictions')(x)
    return keras.Model(inputs, out, name='SE_CNN_MHA_BiLSTM')

model = build_advanced_model(input_shape=(40, 130, 3), num_classes=NUM_CLASSES)
model.summary()

