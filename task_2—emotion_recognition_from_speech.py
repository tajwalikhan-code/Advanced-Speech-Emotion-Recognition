def generate_audio_viz(y_wav, sr, audio_path):
    """
    Generates a 3-panel visualization for any uploaded audio file:
      Panel 1 — Waveform
      Panel 2 — Mel Spectrogram
      Panel 3 — MFCC (channel 0 of 3-ch feature)
    Returns the saved image path.
    """
    fig, axes = plt.subplots(1, 3, figsize=(16, 4))
    fig.suptitle("🔬 Live Audio Feature Analysis", fontsize=13, fontweight='bold')

    # Waveform
    t = np.linspace(0, DURATION, len(y_wav))
    axes[0].plot(t, y_wav, color='#3498db', linewidth=0.8, alpha=0.9)
    axes[0].fill_between(t, y_wav, alpha=0.25, color='#3498db')
    axes[0].set_title("Waveform",   fontweight='bold');  axes[0].set_xlabel("Time (s)")
    axes[0].set_ylabel("Amplitude")

    # Mel Spectrogram
    S    = librosa.feature.melspectrogram(y=y_wav, sr=sr, n_mels=N_MELS,
                                           n_fft=N_FFT, hop_length=HOP_LENGTH)
    S_db = librosa.power_to_db(S, ref=np.max)
    img  = librosa.display.specshow(S_db, sr=sr, hop_length=HOP_LENGTH,
                                     ax=axes[1], cmap='magma',
                                     x_axis='time', y_axis='mel')
    axes[1].set_title("Mel Spectrogram", fontweight='bold')
    plt.colorbar(img, ax=axes[1], format='%+2.0f dB')

    # MFCC (static channel only for clarity)
    mfcc = librosa.feature.mfcc(y=y_wav, sr=sr, n_mfcc=N_MFCC,
                                  n_fft=N_FFT, hop_length=HOP_LENGTH)
    img2 = librosa.display.specshow(mfcc, sr=sr, hop_length=HOP_LENGTH,
                                     ax=axes[2], cmap='viridis',
                                     x_axis='time', y_axis='mel')
    axes[2].set_title("MFCC (40 Coefficients)", fontweight='bold')
    plt.colorbar(img2, ax=axes[2])

    plt.tight_layout()
    out_path = '/tmp/task2_live_viz.png'
    plt.savefig(out_path, dpi=130, bbox_inches='tight')
    plt.close(fig)
    return out_path


def predict_emotion(audio_path):
    """
    Full inference pipeline: load audio → extract 3-ch feature →
    scale → predict → format result + generate visualization.
    Returns (result_text, visualization_image_path).
    """
    if audio_path is None:
        return "❌ Please upload or record an audio file first.", None

    try:
        # Load audio
        y_wav, _  = librosa.load(audio_path, sr=SAMPLE_RATE, duration=DURATION)
        y_wav     = librosa.util.fix_length(y_wav, size=int(SAMPLE_RATE * DURATION))

        # Extract 3-channel feature
        feat_3ch  = extract_3ch_feature(audio_path, augment=False)
        if feat_3ch is None:
            return "❌ Could not process audio. Please try a clearer recording.", None

        # Scale using saved scaler
        feat_flat = feat_3ch.reshape(1, -1)
        feat_s    = scaler.transform(feat_flat).reshape(1, N_MFCC, TIME_STEPS, 3)

        # Predict
        probs         = model.predict(feat_s, verbose=0)[0]
        top5_idx      = np.argsort(probs)[::-1][:5]
        pred_idx      = top5_idx[0]
        pred_emotion  = EMOTION_NAMES[pred_idx]
        pred_emoji    = EMOTION_EMOJI.get(pred_emotion, '🎙️')
        confidence    = probs[pred_idx] * 100

        # Confidence interpretation
        if   confidence >= 75: certainty = "Very Confident"
        elif confidence >= 55: certainty = "Confident"
        elif confidence >= 40: certainty = "Moderate"
        else:                  certainty = "Uncertain"

        # Format result text
        result  = "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n"
        result += "  🎙️  EMOTION RECOGNITION RESULT\n"
        result += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n"
        result += f"  {pred_emoji}  Emotion     :  {pred_emotion.upper()}\n"
        result += f"  📊  Confidence :  {confidence:.1f}%  ({certainty})\n\n"
        result += "  🔝 Top-5 Predictions:\n"
        for rank, idx in enumerate(top5_idx):
            emo    = EMOTION_NAMES[idx]
            emoji  = EMOTION_EMOJI.get(emo, '')
            pct    = probs[idx] * 100
            bar    = '█' * int(pct / 5) + '░' * (20 - int(pct / 5))
            result += f"  {rank+1}. {emoji} {emo:12s} {bar} {pct:5.1f}%\n"
        result += f"\n  🤖 Architecture :  SE-CNN → MHA → BiLSTM\n"
        result += f"  🎯 Test Accuracy :  {test_acc*100:.2f}%\n"
        result += "\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n"
        result += "  ⚠️  Educational project only."

        # Save to session history
        prediction_history.append({
            'emotion': pred_emotion, 'confidence': f"{confidence:.1f}%",
            'file': os.path.basename(audio_path)
        })

        # Generate visualization
        viz_path = generate_audio_viz(y_wav, SAMPLE_RATE, audio_path)
        return result, viz_path

    except Exception as e:
        return f"❌ Error during prediction: {str(e)}", None


def get_history():
    """Returns the session prediction history as a DataFrame."""
    if not prediction_history:
        return pd.DataFrame(columns=['#','Emotion','Confidence','File'])
    df_hist = pd.DataFrame(prediction_history)
    df_hist.index += 1
    df_hist.index.name = '#'
    df_hist.columns    = ['Emotion','Confidence','File']
    return df_hist.reset_index()

